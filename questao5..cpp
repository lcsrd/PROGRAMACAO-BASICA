#include <stdio.h>
#include <stdlib.h>

int main() {

    int a=8, b=4;

    //soma
    printf("\n a soma de %d + %d = %d", a, b, a + b);

    //subtracao
    printf("\n a subtracao de %d - %d = %d", a, b, a - b);

    //multiplicacao
    printf("\n a multiplicacao de %d * %d = %d", a, b, a * b);

    //divisao
    printf("\n a divisao de %d / %d = %d", a, b, a / b);
    
    return 0;

}
